/**
 * VIP 수학 '문제관리' 진도(교육과정) 데이터.
 * 대단원 > 중단원 > 소단원 > 학습주제 4단계. 문제 생성은 추후 학습주제(topicKey) 기준으로 연결.
 * 새 교과는 MATH_CURRICULA 에 추가하면 문제관리 메뉴에서 선택 가능.
 */

export interface MathSubunit {
  소단원명: string;
  학습주제: string[];
}
export interface MathMidunit {
  중단원명: string;
  소단원: MathSubunit[];
}
export interface MathBigunit {
  대단원명: string;
  중단원: MathMidunit[];
}
export interface MathCurriculum {
  교과: string;
  대단원: MathBigunit[];
}

/** 학습주제 1개를 가리키는 안정 키 (문제 저장·조회용). 경로 전체를 ' > ' 로 이어 사용. */
export function mathTopicKey(교과: string, 대단원: string, 중단원: string, 소단원: string, 학습주제: string): string {
  return [교과, 대단원, 중단원, 소단원, 학습주제].join(' > ');
}

