export const MATH_CURRICULA: MathCurriculum[] = [COMMON_MATH_1, COMMON_MATH_2, MATH_SANG, MATH_HA];

/** 한 교과의 전체 학습주제 개수. */
export function countTopics(c: MathCurriculum): number {
  let n = 0;
  for (const big of c.대단원) for (const mid of big.중단원) for (const sub of mid.소단원) n += sub.학습주제.length;
  return n;
}
