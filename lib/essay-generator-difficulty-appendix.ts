/**
 * 서술형 출제기 — Claude user 메시지에 붙이는 난이도별 추가 지시.
 * (assets/exam_kit/generation_prompt.md 공통 규칙 위에 덧씌움)
 */

export type EssayGeneratorDifficulty = '고난도' | '중난도';

const 중난도_추가 = `
[난이도: 중난도 — 반드시 준수]

■ 보기(bogi)와 변형
- 선택한 두 문장의 **정답 문장**은 지문 원문과 동일한 표현·어법으로 유지한다.
- 보기 청크는 **정답 문장을 의미 단위로 잘라낸 조각만** 사용한다. 청크 내 단어·형태는 **원문 그대로** 둔다.
- **의도적 오류·형태 변형(동사원형으로 바꾸기, 분사를 다른 품사로 바꾸기 등)을 보기에 넣지 않는다.** 순서만 섞어 배열하면 정답 문장이 되게 한다.

■ 조건(conditions)
- "보기 중 잘못된 형태를 고쳐 쓸 것" / "변형된 청크를 바로잡을 것" 같은 조건은 **넣지 않는다.**
- 문법 포인트 조건은 **원문 구조를 그대로 쓰게 유도**하는 설명만 쓴다(배열·순서 이해에 집중).
- 마지막 조건군에서 **「보기의 단어(구)를 올바른 순서로 배열하면 완성된다」**는 취지를 분명히 한다.
`.trim();

const 고난도_추가 = `
[난이도: 고난도 — 반드시 준수]

■ 보기(bogi)와 변형
- 각 문항마다 보기 청크 **전체 중 1~2개만** 정답 문장과 다르게 **의도적으로 변형**한다. (나머지 청크는 정답 문장과 동일한 형태·철자로 둔다.)
- 변형 예시(이 중 일부를 활용): 현재분사 **-ing** → **동사원형**, 과거분사 ↔ 현재분사 혼동 유발, **be 동사** 형태만 바꾸기, 단복수·시제 한 단어만 틀리게 하기, 관계사 **who/which/that** 중 한 곳만 부자연스럽게 바꾸기 등.
- 변형은 **학생이 문맥·어법으로 바로잡을 수 있는 수준**으로 하고, 청크 개수·분할 규칙(7~10개 등)은 기존 마스터 프롬프트를 따른다.

■ 조건(conditions) — 매우 중요: 정답 노출 금지
- **조건 본문에 변형된 청크의 단어를 그대로 쓰거나(예: \`combine\`), 올바른 형태(예: \`combines\`)를 직접 써서는 절대 안 된다.** 즉 "보기에 ○○로 제시된 부분은 틀린 형태이므로 ○○로 고쳐 써서 …" 식으로 **틀린 단어 ↔ 정답 단어를 짝지어 노출하지 않는다.**
- 대신 **문법 카테고리 힌트만** 제공하는 한 항목을 추가한다. 예시 패턴:
  - "보기 중 **1~2개**의 청크는 **수 일치(주어-동사 일치)**가 맞도록 형태를 고쳐 써야 한다."
  - "보기 중 **1개**의 청크는 **분사의 능동/수동(현재분사 vs 과거분사)** 관점에서 형태를 바로잡아야 한다."
  - "보기 중 **1~2개**의 청크는 **시제·태**가 문맥에 맞도록 어형을 변환해야 한다."
- 학생이 **어디가 틀렸는지 스스로 식별**한 뒤 **문맥·어법으로 정답 형태를 추론**해야 풀 수 있게 만든다. **위치(몇 번째 청크)도 직접 지정하지 않는다.**
- 나머지 조건은 기존 규칙(의미 일치, 어법, 단어 수, 전체 배열 등)을 따른다.

■ 해설·의도(answer 영역에서만 정답 노출)
- grammar_points / intent_content에서는 **어느 청크가 함정인지, 어떤 형태로 고쳐야 하는지** 구체적으로 설명해도 된다(여기는 답지이므로 정답을 적어도 무방).
- 단, 위의 conditions 영역에는 **정답 형태를 직접 적지 않는다.**
`.trim();

/** UI·문서용 — 난이도별 추가 지시 전문 */
export const ESSAY_DIFFICULTY_APPENDIX_TEXT: Record<EssayGeneratorDifficulty, string> = {
  중난도: 중난도_추가,
  고난도: 고난도_추가,
};

/** 난이도 문자열에 맞는 user 메시지 덧붙임. 알 수 없는 값이면 빈 문자열. */
export function essayGeneratorDifficultyAppendix(difficulty: string): string {
  const d = difficulty.trim();
  if (d === '중난도') return ESSAY_DIFFICULTY_APPENDIX_TEXT.중난도;
  if (d === '고난도') return ESSAY_DIFFICULTY_APPENDIX_TEXT.고난도;
  return '';
}
